package Java.Basics.Arrays;

public class ex1 {
    public static void main(String[] args) {

        int[] arr = {40, 55, 63, 17, 22};  // initializing array
        int n = arr.length;                // size of array

        for (int i = 0; i < n; i++)
            System.out.print(arr[i] + " ");
    }
}